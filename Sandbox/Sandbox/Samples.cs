using System;

namespace Sandbox
{
	public static class Samples
	{
		public static bool BooleanAnd(bool a, bool b)
			=> a & b;

		public static int IntAnd(int a, int b)
			=> a & b;

		public static bool BooleanOr(bool a, bool b)
			=> a | b;

		public static int IntOr(int a, int b)
			=> a | b;
	}
}
